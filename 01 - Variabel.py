# Variabel adalah tempat menyimpan data

# assignment nilai ke variabel
a = 10
x = 5
panjang = 1000

# pemanggilan pertama variable di atas
print("Nilai a = ", a)
print("Nilai x = ", x)
print("Nilai panjang = ", panjang)

# penamaan
nilai_y = 15
juta10 = 1000000
nilaiZ = 17.5

# pemanggilan kedua
print("Nilai a = ", a)
a = 7
print("Nilai a = ", a)

# assignment indirect
b = a
print("Nilai b = ", b)